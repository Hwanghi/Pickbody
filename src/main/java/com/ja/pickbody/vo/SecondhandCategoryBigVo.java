package com.ja.pickbody.vo;

public class SecondhandCategoryBigVo {
	
	private int big_no;
	private String big_name;
	public SecondhandCategoryBigVo(int big_no, String big_name) {
		super();
		this.big_no = big_no;
		this.big_name = big_name;
	}
	public SecondhandCategoryBigVo() {
		super();
	}
	public int getBig_no() {
		return big_no;
	}
	public void setBig_no(int big_no) {
		this.big_no = big_no;
	}
	public String getBig_name() {
		return big_name;
	}
	public void setBig_name(String big_name) {
		this.big_name = big_name;
	}
	
	

}
